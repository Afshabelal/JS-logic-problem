// Bit wise operator

// & (AND operator) 

//  5 & 1 --> 101 & 001 -->001 -->1
//  4 & 1 --> 100 & 001 -->000 -->0

    // 1 0 --> 0 (true&&false) --> false --->0
    // 0 1 --> 0 (false&&true) --> false ---> 0
    // 1 1 --> 1 (true&&true)  --> true ---> 1
    // 0 0 --> 0 (false&&false) ---> false --> 0

    
// | (OR operator)
//  5 | 1 --> 101 | 001 --> 101 --> 5
//  3 | 1 --> 011 | 001 --> 011 --> 3
    // 1 0 --> 1 (true||false) ---> true ---> 1
    // 0 1 --> 1 (false||true) ---> true ---> 1
    // 1 1 --> 1 (true||true) ---> true ---> 1
    // 0 0 ---> 0 (false||false) --> false --> 0

 // ^ (XOR operator)(similar --> false , dissimilar --> true) 
